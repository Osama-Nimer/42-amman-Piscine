#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c[11];
	int		i;

	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	while (nb > 0)
	{
		c[i] = (nb % 10) + '0';
		nb /= 10;
		i++;
	}
	while (i - 1 >= 0)
	{
		write(1, &c[i - 1], 1);
		i--;
	}
}
