void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

#include <stdio.h>

int	main(void)
{
	int a = 5;
	int b = 9;
	ft_swap(&a,&b);
	printf("%d",a);
	printf("%d",b);
}
